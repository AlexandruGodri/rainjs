"use strict";

var configuration = require(process.cwd() + '/lib/configuration');

/**
 * Template data
 */
function index(callback, data) {
    callback(null, data);
}

function level2(callback, data) {
    setTimeout(function () {
        callback(null, data);
    }, Math.floor(Math.random() * 1500));
}

function level3(callback, data) {
    var language = configuration.getLanguage();
    var text;
    switch (language) {
        case 'de_DE':
            text = 'Die aktuelle Plattform Sprache ist Deutsch';
            break;
        case 'ro_RO':
            text = 'Limba curentă a platformei este Română';
            break;
        default:
            text = 'The current platform language is English';
    };
    setTimeout(function () {
        callback(null, {text: text});
    }, Math.floor(Math.random()*3000));
}

function customer_error(callback, data) {
    callback({
        errorMessage: 'This is an error generated by the customer\'s code.'
    }, data);
}

module.exports = {
    index: index,
    level2: level2,
    level3: level3,
    customer_error: customer_error
};
